package com.programmersjail.chemtunesorganicchemistry.liveclass;

public class ReactionModel {

    private String url,date;

    public ReactionModel(String url, String date) {
        this.url = url;
        this.date = date;
    }

    public String getUrl() {
        return url;
    }

    public String getDate() {
        return date;
    }
}
